export function levenshteinDistance(a: string, b: string): number {
    const matrix = []

    for (let i = 0; i <= b.length; i++) {
        matrix[i] = [i]
    }

    for (let j = 0; j <= a.length; j++) {
        matrix[0][j] = j
    }

    for (let i = 1; i <= b.length; i++) {
        for (let j = 1; j <= a.length; j++) {
            if (b.charAt(i - 1) === a.charAt(j - 1)) {
                matrix[i][j] = matrix[i - 1][j - 1]
            } else {
                matrix[i][j] = Math.min(
                    matrix[i - 1][j - 1] + 1, // substitution
                    Math.min(
                        matrix[i][j - 1] + 1, // insertion
                        matrix[i - 1][j] + 1 // deletion
                    )
                )
            }
        }
    }

    return matrix[b.length][a.length]
}

export function isSpellingError(given: string, correct: string): boolean {
    if (!given || !correct) return false

    // Normalize strings (lowercase, trim)
    const normGiven = given.toLowerCase().trim()
    const normCorrect = correct.toLowerCase().trim()

    // Calculate distance
    const dist = levenshteinDistance(normGiven, normCorrect)

    // Calculate similarity percentage based on longer string
    const maxLength = Math.max(normGiven.length, normCorrect.length)
    if (maxLength === 0) return false

    const similarity = 1 - (dist / maxLength)

    // Threshold: 80% similarity (slightly lower than 90% to catch more typos)
    return similarity >= 0.8 && similarity < 1
}
